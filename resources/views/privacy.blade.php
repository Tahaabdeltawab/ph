<p>نقوم بجمع معلومات عنك وعن استخدامك لتطبيقنا بطرق مختلفة:</p>
<ul>
<li>بعض المعلومات التي تزودنا بها مباشرة ، مثل وقت التسجيل</li>
<li>معلومات أخرى نجمعها عنك من خلال ملفات تعريف الارتباط والتقنيات المماثلة الأخرى. لمساعدتنا على العمل بشكل فعال وتزويدك بأفضل المنتجات والخدمات وتجارب التطبيقات التي نستطيع.</li>
<li>نقوم أيضًا بجمع معلومات عنك من شركات أخرى (على سبيل المثال عن طريق شراء بيانات ديموغرافية عنك أو من خلال مواقع التواصل الاجتماعي مثل Facebook و Twitter).</li>
<li>عند الوصول إلى تطبيق الهاتف المحمول الخاص بنا ، قد نقوم بجمع معلومات عنك ، بما في ذلك موقعك الجغرافي.</li>
</ul>

<h3>المعلومات الشخصية وغير الشخصية</h3>
<ul>
<li>المعلومات الشخصية هي المعلومات التي يمكن استخدامها لتحديد هويتك كفرد. يتضمن ذلك معلومات مثل اسمك وعنوان بريدك الإلكتروني وعنوانك البريدي ورقم هاتفك وتاريخ الميلاد الكامل ورقم بطاقة الائتمان والموقع الجغرافي وما إلى ذلك.</li>
<li>المعلومات التي لا يمكن استخدامها في حد ذاتها لتحديد هويتك كفرد لا تعتبر معلومات شخصية. نشير إلى هذا النوع من المعلومات على أنه "معلومات غير شخصية" ، مثل إعداد اللغة في هاتفك ، أو الموقع الذي أتيت منه عند زيارة تطبيقنا.</li>
<li><strong>المعلومات التي تقدمها:</strong> أنت تزودنا بالمعلومات عندما تستخدم تطبيقنا للقيام بأشياء مثل تقديم طلب أو المشاركة في عرض ترويجي أو مسابقة. نقوم بجمع معلومات مثل اسمك وعنوانك ورقم هاتفك ومعلومات بطاقتك الائتمانية وتاريخ ميلادك الكامل ومحتوى اتصالاتك مثل ردود الاستبيانات أو تعليقات مراجعة المنتج ومعلومات طلب الشراء ، بما في ذلك محفوظات طلب الشراء ومعلومات طلب الوظيفة ومعلومات الشحن من الأشخاص الذين ترسل إليهم منتجات ، واسم المستخدم وكلمة المرور ومعلومات عنك.</li>
</ul>
<p>يمكنك دائمًا اختيار عدم تقديم بعض أو كل هذه المعلومات ، على الرغم من أن بعض الميزات أو الخدمات الموجودة في تطبيقنا قد لا تعمل بشكل صحيح نتيجة لذلك ، مثل معالجة طلب أو الاستجابة لطلبك.</p>

