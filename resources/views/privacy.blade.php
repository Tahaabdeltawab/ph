<h3>كيف نستخدم ونشارك المعلومات الخاصة بك؟</h3>
<p>نحن نستخدم معلوماتك ونشاركها لأغراض عديدة ، مثل:</p>
<ul>
<li>إضفاء الطابع الشخصي على تجربتك على تطبيقاتنا</li>
<li>تحسين تطبيقاتنا</li>
<li>قديم منتجاتنا</li>
<li>والتواصل معك بشأن العروض الترويجية أو المنتجات والخدمات التي نعتقد أنك قد تكون مهتمًا بها</li>
</ul>

<p>نستخدم أيضًا معلوماتك ونشاركها لإجراء أبحاث ودراسات السوق حتى نتمكن من الاستمرار في الإعلان والتسويق لك بطريقة هادفة وتطوير منتجات وخدمات جديدة قد تهمك.</p>

<p>نحن شركاء مع شركات أخرى تقوم بجمع ومعالجة بعض المعلومات التي نجمعها عنك وعن استخدامك لمواقعنا من أجلنا. نستخدم أيضًا شركات أخرى لتزويدنا بالخدمات ، وبالتالي يجوز لنا أيضًا مشاركة معلومات عنك معها. نشير إلى هذه الشركات الأخرى كشركاء من الأطراف الثالثة. اعتمادًا على الخدمة التي قد يقدمها شريك خارجي ، قد يقومون بجمع معلومات تعريف شخصية عنك أثناء وجودك على مواقعنا. نحن والشركات التابعة لنا وشركائنا الخارجيين نستخدم معلوماتك للقيام بأشياء مثل:</p>

<ul>
<li>تلبية طلباتك وطلباتك الأخرى فيما يتعلق بتشغيل مواقعنا وتطبيقاتنا.</li>
<li>نوفر لك رعاية المستهلك وخدمة حسابك</li>
<li>إنشاء والحفاظ على ملف تعريف المستهلك الخاص بك ، وتصحيح وتحديث المعلومات الخاصة بك</li>
<li>دمج المعلومات التي نجمعها عنك مع المعلومات التي تقدمها</li>
<li>عرض أو إرسال المعلومات والإعلانات التي نعتقد أنها تتناسب مع اهتماماتك وملفك الشخصي</li>
<li>تسهيل التحديثات على مواقعنا ، وتحسين عمل مواقعنا ، وتجميع الإحصائيات حول مستخدمينا واستخدامك لمواقعنا</li>
<li>تخصيص تجربتك بشكل أفضل على مواقعنا</li>
<li>إدارة الاستطلاعات والمسابقات والعروض الترويجية</li>
<li>معالجة معلومات الدفع</li>
<li>إدارة اتصالات البريد الإلكتروني لدينا</li>
<li>تنظيم أو إدارة أو معالجة أو تقديم خدمات إعلانية</li>
<li>تحليل البيانات نيابة عنا لمساعدتنا في تقديم عروض وإعلانات أكثر صلة لك وللتخلص من تسليم العروض المكررة</li>
<li>إدارة عروضنا الترويجية أو استطلاعات الرأي أو المسابقات</li>
<li>إدارة برامج الإحالة والولاء لدينا</li>
</ul>

<p>نستخدم أيضًا ملفات تعريف الارتباط والتقنيات المماثلة الأخرى لجمع إحصاءات مجمعة (بمعنى أنها لا تحدد هويتك شخصيًا) لمساعدتنا على فهم فعالية أداء مواقعنا بما في ذلك فعالية أي إعلانات وعروض ترويجية قد نقوم بتشغيلها.</p>
<p>نحن نشارك المعلومات المجمعة عنك وعن استخدامك لمواقعنا مع شركات أخرى لقياس أداء الصناعة وتحليل المستهلك وأبحاث السوق والتحليلات. هذه المعلومات لا تحدد هويتك الشخصية</p>

