</ul>

<p>نجمعها من مصادر أخرى: نقوم أيضًا بجمع معلومات عنك من مصادر أخرى. تتضمن هذه المعلومات أشياء مثل اسمك وعنوانك وعنوان بريدك الإلكتروني وتاريخ ميلادك ورقم هاتفك ومعلومات ديموغرافية عنك أو عن أسرتك وسلوك الشراء ومحتوى أي رسالة أو منشورات على صفحاتنا على مواقع التواصل الاجتماعي مثل Facebook و Twitter أو غيرها من الإنترنت المنتديات العامة.</p>

<p>إذا كنت تستخدم منصات الوسائط الاجتماعية مثل Facebook ، بناءً على إعدادات الخصوصية الخاصة بك مع مواقع الوسائط الاجتماعية هذه ، فسنجمع معلومات عنك قمت بإتاحتها من خلال خيارات إعداد خصوصية الوسائط الاجتماعية. نقوم أيضًا بجمع المعلومات من مواقع التواصل الاجتماعي من خلال المكونات الإضافية ، مثل زر "أعجبني" على Facebook وصفحات الوسائط الاجتماعية الخاصة بنا. عندما تنشر تعليقًا على صفحة وسائل التواصل الاجتماعي الخاصة بنا أو إذا "أعجبك" أحد منتجاتنا أو عروضنا الترويجية ، بناءً على إعدادات الخصوصية الخاصة بك مع مواقع التواصل الاجتماعي هذه ، فقد نتلقى معلومات عنك ، مثل اسمك وعنوان بريدك الإلكتروني وجنسك وتاريخ الميلاد والاهتمامات وشبكة الأصدقاء. بالإضافة إلى ذلك ، إذا اخترت ربط حسابات الوسائط الاجتماعية الخاصة بك ، مثل صفحتك على Facebook ، بصفحاتنا على وسائل التواصل الاجتماعي ، فقد يقدمون لنا معلومات حول ملفك الشخصي على وسائل التواصل الاجتماعي ونشاطك اعتمادًا على إعدادات الخصوصية الخاصة بك مع تلك الشركات. تأكد من قراءة القسم الخاص بنا حول الروابط إلى مواقع الطرف الثالث ومراجعة إعدادات الخصوصية لحساب الوسائط الاجتماعية الخاص بك وسياسات الخصوصية لمواقع الوسائط الاجتماعية هذه حتى تفهم كيف تقوم الشركات الأخرى بجمع معلوماتك واستخدامها.</p>

