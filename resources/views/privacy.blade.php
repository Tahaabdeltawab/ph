<p>عند إجراء عملية شراء من خلال تطبيقنا ، فإنك تقدم معلومات الدفع ، مثل رقم بطاقة الائتمان الخاصة بك (أو معلومات Pay Pal) واسمك وعنوان إرسال الفواتير وعنوان الشحن ورمز الأمان المرتبط ببطاقتك الائتمانية. يقوم معالج الدفع التابع لجهة خارجية لدينا بجمع هذه المعلومات لنا ويستخدمها فقط لإكمال معاملتك أو تخزين معلومات الدفع الخاصة بك إذا اخترت تمكين هذه الوظيفة. يشارك معالج الدفع لدينا معلومات الدفع هذه مع البنوك المرتبطة والكيانات الأخرى لمعالجة المدفوعات ، وكذلك لمنع الاحتيال والحد من مخاطر الائتمان.</p>

